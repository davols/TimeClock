package se.dxapps.beta.timeclock.data;

public class Project {
private long RowId;
private int Count;
private String Name;
public long getRowId() {
	return RowId;
}
public void setRowId(long rowId) {
	RowId = rowId;
}
public int getCount() {
	return Count;
}
public void setCount(int count) {
	Count = count;
}
public String getName() {
	return Name;
}
public void setName(String name) {
	Name = name;
}
}
